/**
 * Perform statistical calculations
 */
export function calculateStatistics(inputs: DashboardInputs): StatisticalOutput {
  const range = parseFloat((inputs.maxGrade - inputs.minGrade).toFixed(2));
  const variance = parseFloat((inputs.standardDeviation * inputs.standardDeviation).toFixed(2));

  const successRate = inputs.totalStudents > 0
    ? parseFloat(((inputs.passingStudents / inputs.totalStudents) * 100).toFixed(1))
    : 0;

  let homogeneityStatus = '';
  let homogeneityLevel: 'high' | 'medium' | 'low' = 'medium';
  let homogeneityBadgeColor = '';

  const sd = inputs.standardDeviation;
  if (sd < 2.0) {
    homogeneityStatus = 'انسجام مرتفع جداً (تجانس تام)';
    homogeneityLevel = 'high';
    homogeneityBadgeColor = 'bg-emerald-50 text-emerald-700 border-emerald-200';
  } else if (sd >= 2.0 && sd < 3.5) {
    homogeneityStatus = 'انسجام مقبول (تشتت طبيعي)';
    homogeneityLevel = 'medium';
    homogeneityBadgeColor = 'bg-blue-50 text-blue-700 border-blue-200';
  } else {
    homogeneityStatus = 'انسجام منخفض (تشتت حاد وفوارق ضخمة)';
    homogeneityLevel = 'low';
    homogeneityBadgeColor = 'bg-rose-50 text-rose-700 border-rose-200';
  }

  return {
    range,
    variance,
    successRate,
    homogeneityStatus,
    homogeneityLevel,
    homogeneityBadgeColor
  };
}

