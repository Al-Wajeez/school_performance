/**
 * Generate highly precise custom SVG paths for Normal Distribution Curve (Bell Curve)
 * based on Mean (mu) and SD (sigma).
 * We normalize coordinates to fit beautifully inside the coordinate box.
 * Box Width: 460px, Box Height: 180px, Padding: X=20px, Y=15px
 */
/**
 * Calculate composite risk score (0-10) for the early warning system
 */
export function calculateRiskScore(
  inputs: DashboardInputs,
  successRate: number,
  thresholds = DEFAULT_RISK_THRESHOLDS
): RiskScore {
  const { mean, standardDeviation: sd, maxGrade, minGrade } = inputs;
  const range = maxGrade - minGrade;

  const scoreMean = mean <= thresholds.meanDanger ? 2.5 : mean <= thresholds.meanWarning ? 1.5 : 0;
  const scoreSd = sd >= thresholds.sdDanger ? 2.5 : sd >= thresholds.sdWarning ? 1.5 : 0;
  const scoreSuccess = successRate <= thresholds.successRateDanger ? 2.5 : successRate <= thresholds.successRateWarning ? 1.5 : 0;
  const scoreRange = range >= thresholds.rangeDanger ? 2.5 : range >= thresholds.rangeWarning ? 1.5 : 0;

  const total = parseFloat(Math.min(10, scoreMean + scoreSd + scoreSuccess + scoreRange).toFixed(1));
  const level: 'safe' | 'moderate' | 'danger' = total >= 7 ? 'danger' : total >= 4 ? 'moderate' : 'safe';

  return { total, level, breakdown: { meanScore: scoreMean, sdScore: scoreSd, successRateScore: scoreSuccess, rangeScore: scoreRange } };
}

export function generateBellCurveData(mean: number, sd: number) {
  const width = 460;
  const height = 180;
  const paddingX = 30;
  const paddingY = 20;

  const chartWidth = width - paddingX * 2;
  const chartHeight = height - paddingY * 2;

  // To protect our rendering from standard deviation = 0 (infinite peak or crashing div by zero)
  // we clamp sd to a safe minimum of 0.3 for the mathematics of drawing.
  const safeSd = Math.max(0.4, sd);

  // Calculate points for the bell curve
  // $f(x) = e^[-0.5 * ((x-mean)/sd)^2] / (sd * sqrt(2*pi))$
  const pdf = (x: number) => {
    const exponent = -0.5 * Math.pow((x - mean) / safeSd, 2);
    // Standard normal divisor but we optionally scale it slightly to make it visually aesthetic:
    return Math.exp(exponent) / (safeSd * Math.sqrt(2 * Math.PI));
  };

  // We want to sample points along X axis from 0 to 20
  const totalPoints = 120;
  const step = 20 / totalPoints;
  const rawPoints: { x: number; y: number; px: number; py: number }[] = [];

  // Find maximum y value to calibrate visual scale height
  let maxRawY = 0;
  for (let i = 0; i <= totalPoints; i++) {
    const x = i * step;
    const y = pdf(x);
    if (y > maxRawY) maxRawY = y;
  }

  // If curve is extremely narrow, maxRawY is very high, if wide, maxRawY is low.
  // We want the peak to be exactly at chartHeight * 0.85 (so there is margin on top)
  const yScale = maxRawY > 0 ? (chartHeight * 0.85) / maxRawY : 1;

  for (let i = 0; i <= totalPoints; i++) {
    const x = i * step;
    const y = pdf(x);

    const px = paddingX + (x / 20) * chartWidth;
    // SVG coordinates start at top left (y=0 is top), so we invert height
    const py = height - paddingY - y * yScale;

    rawPoints.push({ x, y, px, py });
  }

  // Construct SVGs path strings
  // 1. Overall Bell line
  let bellLinePath = `M ${rawPoints[0].px} ${rawPoints[0].py}`;
  for (let i = 1; i < rawPoints.length; i++) {
    bellLinePath += ` L ${rawPoints[i].px} ${rawPoints[i].py}`;
  }

  // 2. Shaded range for Failing area (x < 10)
  // 3. Shaded range for Passing area (x >= 10)
  const failPoints = rawPoints.filter(p => p.x < 10);
  const passPoints = rawPoints.filter(p => p.x >= 10);

  // We need to add the boundary exactly at x = 10 to make a clean vertical division split!
  const p10_y = pdf(10);
  const p10_px = paddingX + (10 / 20) * chartWidth;
  const p10_py = height - paddingY - p10_y * yScale;

  // Check paths
  let pfPath = '';
  if (failPoints.length > 0) {
    pfPath = `M ${failPoints[0].px} ${height - paddingY}`;
    for (const p of failPoints) {
      pfPath += ` L ${p.px} ${p.py}`;
    }
    // inject the sharp standard limit at exactly 10
    pfPath += ` L ${p10_px} ${p10_py}`;
    pfPath += ` L ${p10_px} ${height - paddingY} Z`;
  }

  let psPath = '';
  if (passPoints.length > 0) {
    psPath = `M ${p10_px} ${height - paddingY}`;
    psPath += ` L ${p10_px} ${p10_py}`;
    for (const p of passPoints) {
      psPath += ` L ${p.px} ${p.py}`;
    }
    psPath += ` L ${rawPoints[rawPoints.length - 1].px} ${height - paddingY} Z`;
  }

  // Standard Deviation boundary markers: Mean - SD and Mean + SD
  const mMinusSd_x = Math.max(0, mean - sd);
  const mPlusSd_x = Math.min(20, mean + sd);

  const mMinusSd_px = paddingX + (mMinusSd_x / 20) * chartWidth;
  const mPlusSd_px = paddingX + (mPlusSd_x / 20) * chartWidth;

  const mean_px = paddingX + (mean / 20) * chartWidth;
  const mean_py = height - paddingY - pdf(mean) * yScale;

  return {
    width,
    height,
    paddingX,
    paddingY,
    chartWidth,
    chartHeight,
    bellLinePath,
    pfPath,
    psPath,
    mean_px,
    mean_py,
    mMinusSd_px,
    mPlusSd_px,
    rawPoints
  };
}

